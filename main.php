<h2>Informacje o Szczecinie!</h2>
<img src="szczecin8.jpg" alt="szczecin" />
<p>Szczecin jest największym miastem województwa zachodniopomorskiego i zarazem jego stolicą. Jest trzecim największym miastem w Polsce, do której należy od 1945 roku. Leży na Pobrzeżu Szczecińskim, nad Odrą i jeziorem Dąbie, a jego lokalizacja sprawia, ze ma duże znaczenie dla gospodarki Szczecin jest także dużym ośrodkiem akademickim i turystycznym. Leży na Europejskim Szlaku Gotyku Ceglanego. Wśród wielu zabytków należy wymienić Zamek Książąt Pomorskich, Basztę Siedmiu Płaszczy oraz Bramy Portową i Królewską. W mieście są też kościoły pochodzące ze średniowiecza. Ciekawostką Szczecina są okazałe rezydencje jak np. Pałac Pod Głowami, Pałac pod Globusem, Pałac Joński, Pałac Sejmu Stanów Pomorskich.</p>

